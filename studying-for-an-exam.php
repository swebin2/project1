              <h2 class="ui-title-block">Studying <strong> for an Exam  </strong></h2>
              <div class="wrap-subtitle">
                <div class="ui-subtitle-block ui-subtitle-block_w-line">
 
<p>No amount Planning and preparation can guarantee success unless you
 tenaciously execute the plan to its fullness.  Read further for a few tips 
to make best use of your study time.</p>


<p><strong style="text-decoration:underline;">Realistic plan:</strong> Make an outline to structure your study plan. You will need to make a 
realistic study plan based on what test you are taking, and the amount of 
time. Sit down with your study materials, and make an outline of the big 
topics/areas you need to study for the test.  Students could cut their
 study time by at least 30% by using a well-planned study schedule or 
time table.</p>


<p><strong style="text-decoration:underline;">Make a List of Books & Notes:</strong> Once you have a sense of the syllabus, make a list of the 
books or study material you would be requiring for the exam. Start with 
the most important material and work toward the less important material to
 make sure the important content gets covered before the test. Prepare 
notes – Whatever you study, try maintaining a notebook for it. Go through
 this notebook at the time of revision.</p>


<p><strong style="text-decoration:underline;">Topic & Subject Selection:</strong> You can start with the most difficult or boring subject 
when you are fully alert and pick up an easier or most interesting one when 
you are feeling down. Do not schedule similar subjects one after another: It
 has been found that it is better put contrasting subjects one after another 
rather than clubbing similar subjects together.</p>



<p><strong style="text-decoration:underline;">Fundamentals First:</strong> Be thorough with the fundamentals and adopt the right strategy: 
Without getting the fundamentals right, it is difficult to proceed with the
 later concepts of the subject. Therefore, work very hard if you are not very 
good at the basics. No matter how hard it is or how much time it take, it is 
worth the effort! The rest of the journey becomes easy once you 
accomplish this!</p>


<p><strong style="text-decoration:underline;">Focus on Strength:</strong>  Focus on your strengths: Try the SWOT Analysis. SWOT stands for 
Strengths, Weaknesses, Opportunities and Threats. Through this 
technique, you will know where your strengths lie and where exactly 
you go wrong. Work hard to overcome your weaknesses.  Analyze as 
to how you can minimize them.  Polish your strengths and try to 
perfect them.</p>


<p><strong style="text-decoration:underline;">Quantitative aptitude:</strong> Try practicing questions daily. Go through the concepts first and 
then solve problems. Make a note of important formulas, theories and 
corollaries. You should be thorough with tables, square roots and cube roots 
to do calculations quickly.



<p><strong style="text-decoration:underline;">Mind Maps:</strong> Create Mental Associations: The ability to make connections is not only an 
easier way to remember information, but it’s the fuel of 
creativity and intelligence. Mind Maps are an easy way to 
connect ideas by creating a visual overview of different 
connections.</p>



<p><strong style="text-decoration:underline;">Get familiar with the pattern:</strong> Get yourself acquainted with the pattern of the exams you 
may be taking up at TrickyScore.com. This will help you solve the paper 
better and be more confident in your approach. Attend mock tests and see 
how you fare in them. Take them seriously and improve upon your 
performance each time.</p>



<p><strong style="text-decoration:underline;">Revision:</strong> Daily revision – When you get up in the morning just take your notes from 
previous day and revise them within a short period of  20-30 minutes. Try 
to remember the problems you had solved on your previous day, the 
formulas you had memorized, any mistakes you did and any other notes you 
might have. You should also revise whatever you have studied in a day just 
before you go to sleep. 
Weekly revision – This will require a little more time than your daily 
revision. Ideally  you should keep a day in the week to only revise whatever 
you have studied and use the remaining time to relax. Go through all your 
notes, formulas, and mistakes types of problems you have studied over the 
week and see if you need to put more efforts in a particular topic. You 
should also use this opportunity to judge if you are progressing at a good 
speed and will be able to cover the topics on time."</p>


<p><strong style="text-decoration:underline;">Regular studies everyday:</strong> Since we have no external time table or anyone to monitor us, 
we need to have our own time schedules to study. The best times is like in 
the early mornings before college or office hours and again in the evenings 
if possible. Regular touch with books and syllabus is essential till appearing
 for exam. But do not neglect health, diet and proper rest.</p>


<p><strong style="text-decoration:underline;">Shorter study sessions & Regular breaks:</strong> Shorter study sessions work better than 
marathon sessions: Psychologists have found out that students learn as 
much in one-hour sessions spread over four days as they learn in one six-
hour marathon session. Regular breaks - Develop a study routine that 
works for you. There is no point in in over burdening yourself. Regular 
breaks will help you to relax and rejuvenate. A minimum of six hours of 
sleep is very important. Also do ensure that your body gets sufficient exercise.</p>


<p>Practice, Practice, Practice… The ability to read and understand the 
questions faster is habituated by these tests. So attend more and more 
TrickyScore tests…</p>


<p><strong style="text-decoration:underline;">Power Reading – OK4R:</strong> Dr. Walter Pauk has devised the OK4R method, which can help 
you pack more information through power reading. This is to help you to 
ace Competitive Entrance Exams. Here is how you go about it:</p>


<p><strong style="text-decoration:underline;">O for Overview:</strong> In the first glance, just read the headings, sub-
headings, introductory  paragraph, and summary at the end of the chapter. It will give you 
general idea of what is included in the chapter.</p>

<p><strong style="text-decoration:underline;">K for Key ideas:</strong> Now, go back to the beginning of the chapter and try to skim through
 the key ideas. First sentence of each paragraph, italics and bold-type text, tables, pictures
 and diagrams, bulleted sections and itemizations often present key ideas of the chapter. </p>

<p><strong style="text-decoration:underline;">R1 for Reading the topic or chapter from beginning to end:</strong> Now that you already 
know what is being discussed, read through the entire chapter and see full explanation of 
the ideas that have been presented and how they have been explained.</p>

<p><strong style="text-decoration:underline;">R2 for Recall: </strong>Now put your book aside and write down major points of what you 
have read and make your notes in few words or sentences. Immediate recall will only take
 a minute or two but doubles up retention time of the topic you are studying. </p>

<p><strong style="text-decoration:underline;">R3 for Reflect:</strong> Now that you have kept the material in the storage unit of your 
memory it, sift it to put it in your permanent memory. Think over it and try to find
 significance of what you have read and its relationship with other topics you have learnt.</p>

<p><strong style="text-decoration:underline;">   R4 for Review or Revision:</strong> On weekends, you can test yourself on what you have 
learned throughout the week. These time-to-time reviews or revisions help you to fix the 
information forever.
</p>  
